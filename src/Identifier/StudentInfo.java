package Identifier;

public class StudentInfo {
    // Method untuk menerima nilai dalam bentuk String,int dan double
    public String showStudentInfo(String name, int age, double gpa) {
        // Mengembalikan nilai dari variabel di atas
        return "Name: " + name + "\n" +
                "Age : " + age + "\n" +
                "GPA : " + gpa + "\n";

    }
}
